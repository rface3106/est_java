package Hr0603;

public class Switch2 {
    public static void main(String[] args) {
        String grade = "B";

        switch (grade) {
            case "A":
                System.out.println("Excellent");
                break;
            case "B":
                System.out.println("good");
                break;
            case "C":
                System.out.println("average");
                break;
            case "D":
                System.out.println("below average");
                break;
            case "F":
                System.out.println("fail");
                break;


        }
    }
}

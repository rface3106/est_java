package Hr0603;

public class Switch {
    public static void main(String[] args) {
        String day = "일";

        switch (day) {
            case "월":
                System.out.println("책 읽기");
                break;
            case "화":
                System.out.println("잠자기");
                break;
            case "수":
                System.out.println("영화보기");
                break;
            case "목":
                System.out.println("운동하기");
                break;
            case "금":
                System.out.println("놀기");
                break;
            case "토":
                System.out.println("공부하기");
                break;
            case "일":
                System.out.println("놀러가기");
                break;


        }
    }
}

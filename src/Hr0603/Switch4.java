package Hr0603;

public class Switch4 {
    public static void main(String[] args) {
        String color = "Red";

        switch (color){
            case "Red":
                System.out.println("Apple");
                break;
            case "Green":
                System.out.println("Grass");
                break;
            case "Blue":
                System.out.println("Sky");
                break;
        }
    }
}
